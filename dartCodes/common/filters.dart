class VocabularyModel {
  final int? id;
  final String englishPronounciation;
  final String hindiPronounciation;
  final String urduWord;
  final String origin;
  final String englishWord;
  final String hindiWord;
  final String useInTense;
  final String category;
  final int correctCount;

  VocabularyModel(
      {this.id,
      required this.englishPronounciation,
      required this.hindiPronounciation,
      required this.urduWord,
      required this.origin,
      required this.englishWord,
      required this.hindiWord,
      required this.useInTense,
      required this.category,
      required this.correctCount});
}

void main() {
  final getList = VocabData();

  List<int> stats = [0, 0, 0, 0, 0, 0];

  List<VocabularyModel> initiatedLearning = [];
  List<VocabularyModel> stillLearning = [];
  List<VocabularyModel> approachingProficiency = [];
  List<VocabularyModel> achievedLearning = [];
  List<VocabularyModel> reacallMode = [];
  List<VocabularyModel> accomplished = [];

  for (var i = 0; i < 20; i++) {
    var element = getList.vocabList[i];
    switch (element.category) {
      case 'InitiatedLearning':
        initiatedLearning.add(element);
        stats[0] += 1;
        break;
      case 'StillLearning':
        stillLearning.add(element);
        stats[1] += 1;
        break;
      case 'ApproachingProficiency':
        approachingProficiency.add(element);
        stats[2] += 1;
        break;
      case 'AchievedProficiency':
        achievedLearning.add(element);
        stats[3] += 1;
        break;
      case 'RecallMode':
        reacallMode.add(element);
        stats[4] += 1;

        break;
      case 'Accomplished':
        accomplished.add(element);
        stats[5] += 1;
        break;
      default:
    }
  }

  print(stats);
  print(initiatedLearning);
}
